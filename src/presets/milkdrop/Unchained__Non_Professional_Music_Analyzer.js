function Unchained__Non_Professional_Music_Analyzer() {
  with (Math) {
    fRating=3;
    gamma=2;
    decay=0.9;
    echo_zoom=0.99663;
    echo_alpha=0.5;
    echo_orient=0;
    wave_mode=0;
    wave_additive=0;
    wave_usedots=0;
    bWaveThick=0;
    bModWaveAlphaByVolume=0;
    wave_brighten=1;
    wrap=0;
    darken_center=0;
    bRedBlueStereo=0;
    brighten=0;
    darken=0;
    solarize=0;
    invert=0;
    wave_a=1.958904;
    fWaveScale=0.01;
    fWaveSmoothing=0.54;
    wave_mystery=-0.46;
    fModWaveAlphaStart=0.75;
    fModWaveAlphaEnd=0.95;
    warp_speed=1;
    warp_scale=1;
    zoomexp=1;
    fShader=0;
    zoom=1;
    rot=0;
    cx=0.5;
    cy=0.5;
    dx=0;
    dy=0;
    warp=1;
    sx=1;
    sy=1;
    wave_r=0.5;
    wave_g=0.5;
    wave_b=0.5;
    wave_x=0.5;
    wave_y=0.5;
    ob_size=0.005;
    ob_r=0;
    ob_g=0;
    ob_b=0;
    ob_a=1;
    ib_size=0;
    ib_r=0.25;
    ib_g=0.25;
    ib_b=0.25;
    ib_a=0;
    mv_x=24.959999;
    mv_y=19.199999;
    mv_dx=0;
    mv_dy=0;
    mv_l=0.85;
    mv_r=0.4999;
    mv_g=0.4999;
    mv_b=0.4999;
    mv_a=0;
    old_bass_flop=bass_flop;
    old_treb_flop=treb_flop;
    old_mid_flop=mid_flop;
    chaos=.9+.1*sin(pulse);
    bass_thresh = above(bass_att,bass_thresh)*2 + (1-above(bass_att,bass_thresh))*((bass_thresh-1.6)*chaos+1.6);
    bass_flop=abs(bass_flop-equal(bass_thresh,2));
    treb_thresh=above(treb_att,treb_thresh)*2 + (1-above(treb_att,treb_thresh))*((treb_thresh-1.6)*chaos+1.6);
    treb_flop=abs(treb_flop-equal(treb_thresh,2));
    mid_thresh=above(mid_att,mid_thresh)*2 + (1-above(mid_att,mid_thresh))*((mid_thresh-1.6)*chaos+1.6);
    mid_flop=abs(mid_flop-equal(mid_thresh,2));
    bass_changed=bnot(equal(old_bass_flop,bass_flop));
    mid_changed=bnot(equal(old_mid_flop,mid_flop));
    treb_changed=bnot(equal(old_treb_flop,treb_flop));
    bass_residual = bass_changed*sin(pulse*3) + bnot(bass_changed)*bass_residual;
    treb_residual = treb_changed*sin(pulse*3) + bnot(treb_changed)*treb_residual;
    mid_residual = mid_changed*sin(pulse*3) + bnot(mid_changed)*mid_residual;
    monitor=warp/100;
    pulse=if_milk(above(abs(pulse),20),-20,pulse+(bass_thresh+mid_thresh+treb_thresh)*.032);
    entropy=if_milk(bass_changed*mid_changed*treb_changed,bass_flop+treb_flop+mid_flop+random_integer(5),entropy);
    q1=mid_residual;
    q2=bass_residual;
    q3=treb_residual;
    q4=sin(pulse);
    q5=cos(pulse*(.5+.1*entropy));
    q6=sin(pulse*(.5+pow(.25,entropy)));
    q7=above(q1,0)+above(q2,0)+above(q3,0)+above(q3,0)*treb_flop+above(q2,0)*bass_flop+above(q1,0)*mid_flop;
    q8=entropy;
    mv_r=.5+.5*sin(q4*.3+time*1.678);
    mv_b=.5+.5*sin(q4*.3+time*.789);
    mv_g=.5+.5*sin(q5*.3+time*.456);
    mv_a=.3+.3*sin(time*1.178+q5*1.14);
    wave_r=1-if_milk(above(q7,3),ib_g,ob_g);
    wave_b=1-if_milk(below(q7,3),ib_r,ob_r);
    wave_g=1-if_milk(q7%3,ib_b,ob_b);
    wave_y=.25+.25*q2+.5*bnot(frame%3);
    wave_mystery=wave_mystery+.05*q3;
    wave_x=wave_x+.01*q4;;
    warp=0;
    rot=0;
    decay=.905+.005*q3;
    wave_mode=4*bnot(frame%3);
    // per_pixel_1=zoom = zoom + (q1*.005);
    // per_pixel_2=dy = if_milk(below(y,.5+.5*q1),-.01*random_integer(10),0);
    // per_pixel_3=rot=.01*sin((y-.5)*3.14*q2);
  }
}
register_preset( Unchained__Non_Professional_Music_Analyzer);
