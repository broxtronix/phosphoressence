    wave_a=1;
    fWaveScale=0.054279;
    fWaveSmoothing=0.75;
    wave_mystery=0;
    fModWaveAlphaStart=0.85;
    fModWaveAlphaEnd=1.95;
    warp_speed=1;
    warp_scale=1;
    zoomexp=1;
    fShader=0;
    zoom=1;
    rot=0;
    cx=0.5;
    cy=0.5;
    dx=0;
